const pricingBtn1 = document.getElementById('pricingButton1');

const mouseOverPricing1 = () => {
    pricingBtn1.style.backgroundColor = '#36536b';
    pricingBtn1.style.color = '#fbfcfe';
    pricingBtn1.style.cursor = 'pointer';
};

const mouseOutPricing1 = () => {
    pricingBtn1.style.backgroundColor = 'transparent';
    pricingBtn1.style.color = '#36536b';
};

pricingBtn1.addEventListener('mouseover', mouseOverPricing1);
pricingBtn1.addEventListener('mouseout', mouseOutPricing1);



